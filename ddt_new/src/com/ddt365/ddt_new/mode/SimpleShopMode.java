package com.ddt365.ddt_new.mode;

public class SimpleShopMode {
	private String b_name;
	private String bid;
	private String b_pic;
	private String district;
	private boolean is_validate;
	private boolean has_gz;
	
	public String getB_name() {
		return b_name;
	}
	public void setB_name(String b_name) {
		this.b_name = b_name;
	}
	public String getBid() {
		return bid;
	}
	public void setBid(String bid) {
		this.bid = bid;
	}
	public String getB_pic() {
		return b_pic;
	}
	public void setB_pic(String b_pic) {
		this.b_pic = b_pic;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public boolean isIs_validate() {
		return is_validate;
	}
	public void setIs_validate(boolean is_validate) {
		this.is_validate = is_validate;
	}
	public boolean isHas_gz() {
		return has_gz;
	}
	public void setHas_gz(boolean has_gz) {
		this.has_gz = has_gz;
	}
}
